#include "libft.h"

int		ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 0;
	while (sqrt < nb)
	{
		if (sqrt * sqrt == nb)
			return (sqrt);
		sqrt++;
	}
	return (0);
}
